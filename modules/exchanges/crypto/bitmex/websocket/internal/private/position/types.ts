export interface PositionSource {
  account: number;
  symbol: string;
  // 全仓保证金，也被称为 “跨期保证金”，是指利用所有的可用余额来避免强制平仓。
  crossMargin: boolean;
  // 佣金
  commission: number;
  // 杠杆
  leverage: number;
  // 执行数量
  execQty: number;
  // 平均成本价格
  avgCostPrice: number;
  // 平均入场价格
  avgEntryPrice: number;
  // 保本价格(含手续费)
  breakEvenPrice: number;
  // 强平价格
  liquidationPrice: number;
  // 破产价格
  bankruptPrice: number;
  lastPrice: number;
  // 保证金通知价格
  marginCallPrice: number;
  // 是否持仓
  isOpen: boolean;
  // 持仓数量
  openingQty: number;
  // 持仓成本(-484825: -0.00484825 XBT)
  openingCost: number;
  // 持仓佣金(844: 0.00000844 XBT)
  openingComm: number;
  // 已实现盈亏(-844: -0.00000844 XBT)
  realisedPnl: number;
  // 未实现盈亏(1325: 0.00001325 XBT)
  unrealisedPnl: number;
  // 自仓位平仓以来已转移到您的钱包的已实现PNL的价值。
  prevRealisedPnl: number;
  // 起始保证金
  initMargin: number;
  // 维持保证金(96061: 0.00096061 XBT)
  maintMargin: number;
  timestamp: string;
}
